export function Padbank({volume}){


    const [bank, setBank] = useState(false);

    const handleBank = () => {
      setBank(!bank)
    }

    return(

        <Fragment>
        <div className="pad-container">

            <div className="bank-control">
                <p>{`${bank ? "SoundPad: 2" : "SoundPad: 1"}`}</p>
                <div className="select">
                    <div className="inner" style={bank ? {float: "right"} : {float: "left"}} onClick={handleBank}></div>
                </div>
            </div>

            <div className="pad-bank">
                {(bank ? bankTwo : bankOne).map((sound) => (
                  <SoundPad key={sound.id} sound={sound} volume={volume}/>
                ))}
            </div>
        </div>
        </Fragment>
    );
}

//soundpad component
export function SoundPad({sound, volume}) {


  const playSound = () => {
    const soundTag = document.getElementById(sound.id);
    soundTag.volume = volume;
    soundTag.currentTime = 0;
    soundTag.play();
  }

  const handleKeyPress = (e) => {
    if(e.keyCode === sound.keyCode){
      playSound();
    }
  }


  useEffect(() => {
    document.addEventListener('keydown', handleKeyPress);
    return () => {
      document.removeEventListener('keydown', handleKeyPress);
    }
  });

  return(

    <Fragment>
      
      <div className="sound-pad" onClick={playSound}>
      <audio src={sound.url} id={sound.id} key={sound.id}></audio>
      {sound.keyTrigger}
      </div>
    </Fragment>
  );
}